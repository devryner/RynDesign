import type { GeneratorContext, GeneratedFile, ResolvedComponent } from '@ryndesign/plugin-api';
import type { AndroidViewGeneratorOptions } from './index.js';

export function generateComponentFile(
  comp: ResolvedComponent,
  ctx: GeneratorContext,
  options: AndroidViewGeneratorOptions
): GeneratedFile[] {
  const { definition } = comp;
  const name = `DS${definition.name}`;
  const pkg = options.packageName ?? 'com.ryndesign.components';
  const files: GeneratedFile[] = [];

  // Generate Kotlin custom View class
  files.push({
    path: `components/${name}.kt`,
    content: generateCustomView(comp, options, pkg),
    language: 'kotlin',
  });

  // Generate XML layout
  files.push({
    path: `layout/view_${snakeCase(definition.name)}.xml`,
    content: generateXmlLayout(comp, options),
    language: 'xml',
  });

  // Generate XML attrs for the custom view
  files.push({
    path: `values/attrs_${snakeCase(definition.name)}.xml`,
    content: generateCustomAttrs(comp),
    language: 'xml',
  });

  return files;
}

