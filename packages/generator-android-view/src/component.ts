function generateXmlLayout(comp: ResolvedComponent, options: AndroidViewGeneratorOptions): string {
  const { definition } = comp;

  let xml = `<?xml version="1.0" encoding="utf-8"?>\n`;
  xml += `<!-- Auto-generated by @ryndesign/generator-android-view -->\n`;
  xml += `<merge xmlns:android="http://schemas.android.com/apk/res/android"\n`;
  xml += `    xmlns:tools="http://schemas.android.com/tools">\n\n`;

  xml += `    <LinearLayout\n`;
  xml += `        android:id="@+id/content_layout"\n`;
  xml += `        android:layout_width="wrap_content"\n`;
  xml += `        android:layout_height="wrap_content"\n`;
  xml += `        android:layout_gravity="center"\n`;
  xml += `        android:gravity="center"\n`;
  xml += `        android:orientation="horizontal">\n\n`;

  if (definition.slots.leadingIcon) {
    xml += `        <ImageView\n`;
    xml += `            android:id="@+id/leading_icon"\n`;
    xml += `            android:layout_width="20dp"\n`;
    xml += `            android:layout_height="20dp"\n`;
    xml += `            android:layout_marginEnd="8dp"\n`;
    xml += `            android:scaleType="fitCenter"\n`;
    xml += `            android:visibility="gone"\n`;
    xml += `            tools:visibility="visible" />\n\n`;
  }

  if (definition.props.label) {
    xml += `        <TextView\n`;
    xml += `            android:id="@+id/title_text"\n`;
    xml += `            android:layout_width="wrap_content"\n`;
    xml += `            android:layout_height="wrap_content"\n`;
    xml += `            android:gravity="center"\n`;
    xml += `            android:textSize="16sp"\n`;
    xml += `            tools:text="${definition.name}" />\n\n`;
  }

  if (definition.slots.trailingIcon) {
    xml += `        <ImageView\n`;
    xml += `            android:id="@+id/trailing_icon"\n`;
    xml += `            android:layout_width="20dp"\n`;
    xml += `            android:layout_height="20dp"\n`;
    xml += `            android:layout_marginStart="8dp"\n`;
    xml += `            android:scaleType="fitCenter"\n`;
    xml += `            android:visibility="gone"\n`;
    xml += `            tools:visibility="visible" />\n\n`;
  }

  xml += `    </LinearLayout>\n\n`;
  xml += `</merge>\n`;

  return xml;
}

function generateCustomAttrs(comp: ResolvedComponent): string {
  const { definition } = comp;
  const name = `DS${definition.name}`;

  let xml = `<?xml version="1.0" encoding="utf-8"?>\n`;
  xml += `<!-- Auto-generated by @ryndesign/generator-android-view -->\n`;
  xml += `<resources>\n\n`;

  xml += `    <declare-styleable name="${name}">\n`;

  if (definition.props.label) {
    xml += `        <attr name="ds_label" format="string" />\n`;
  }

  if (definition.props.disabled) {
    xml += `        <attr name="ds_disabled" format="boolean" />\n`;
  }

  for (const [variantName, variant] of Object.entries(definition.variants)) {
    xml += `        <attr name="ds_${variantName}" format="enum">\n`;
    variant.values.forEach((v, i) => {
      xml += `            <enum name="${v}" value="${i}" />\n`;
    });
    xml += `        </attr>\n`;
  }

  xml += `    </declare-styleable>\n\n`;
  xml += `</resources>\n`;

  return xml;
}

function snakeCase(str: string): string {
  return str.replace(/([a-z])([A-Z])/g, '$1_$2').toLowerCase();
}

function capitalize(str: string): string {
  return str.charAt(0).toUpperCase() + str.slice(1);
}
